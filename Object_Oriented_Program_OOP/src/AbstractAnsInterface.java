
public class AbstractAnsInterface {
	/*
	 abstract class:
	 - có các phương thức abstract và non-abstract.
	 - không hỗ trợ đa kế thừa.
	 - có các biến final, non-final, static, non-static.
	 - có thể cung cấp nội dung cài đặt cho phương thức của interface.
	 - sử dụng từ khóa abstract để khai báo abstract class.
	 ----------------------------------------------------------
	 interface:
	 - chỉ có phương thức abstract class.
	 - có hỗ trợ đa kế thừa.
	 - chỉ có các biến static và final ( hoặc cả 2).
	 - không cung cấp nội dung cài đặt cho phương thức của abstract class.
	 - sử dụng từ khóa interface để khai báo cho interface.
	 
	 
	 ### Có thể hiểu abstract class là trừu tượng 1 phần ( <= 100%) , và interface là trừu tượng toàn phần ( 100 %).
	 */
}
